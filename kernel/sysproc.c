uint64
sys_sigalarm(void)
{
    int cycle;
    uint64 faddr;
    argint(0, &cycle);
    argaddr(1, &faddr);
    struct proc *p = myproc();
    p->cycle = cycle;
    p->faddr = faddr;
//    printf("cycle %d faddr %p", cycle, faddr);
    return 0;
}

uint64
sys_sigreturn(void)
{
    struct proc *p = myproc();

    *p->trapframe = *p->_trapframe;
    p->ret = 1;
//    printf("222 stap %p kstack %p tp %p pgtable %p\n", r_satp(), p->kstack, r_tp(), p->pagetable);
    return 0;
}

uint64
sys_exit(void)
{
  int n;
  if(argint(0, &n) < 0)
    return -1;
  exit(n);
  return 0;  // not reached
}

uint64
sys_getpid(void)
{
  return myproc()->pid;
}

uint64
sys_fork(void)
{
  return fork();
}

uint64
sys_wait(void)
{
  uint64 p;
  if(argaddr(0, &p) < 0)
    return -1;
  return wait(p);
}

uint64
sys_sbrk(void)
{
  int addr;
  int n;

  if(argint(0, &n) < 0)
    return -1;
  addr = myproc()->sz;
  if(growproc(n) < 0)
    return -1;
  return addr;
}

uint64
sys_sleep(void)
{
  int n;
  uint ticks0;
  printf("backtrace\n");
  backtrace();
  if(argint(0, &n) < 0)
    return -1;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(myproc()->killed){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  if(argint(0, &pid) < 0)
    return -1;
  return kill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}
